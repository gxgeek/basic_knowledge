public class Solution  {
    public int firstBadVersion(int n) {
        int leaf = 1;
        int right = n;
        while (leaf+1<right){
            int mid = leaf + (right - leaf) / 2;
            boolean badVersion = isBadVersion(mid);
            if (badVersion){
                right = mid;
            }else {
                leaf = mid;
            }
        }

        if (isBadVersion(leaf)){
            return leaf;
        }
        if (isBadVersion(right)) {
            return right;
        }
        return -1;
        
    }

    private boolean isBadVersion(int mid) {
        return mid>=4;
    }
}
