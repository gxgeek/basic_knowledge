class Solution {
    public int[] relativeSortArray(int[] arr1, int[] arr2) {
        int index = 0;
        int[] base = new int[1001];
        for (int i = 0; i<arr1.length; i++) {
            base[arr1[i]]++;
        }

        for (int i = 0; i < arr2.length ; i++){
            while (base[arr2[i]] > 0) {
                arr1[index++] = arr2[i];
                base[arr2[i]]--;
            }
        }
        for (int i = 0; i < base.length; i++) {
            while (base[i] >= 0){
                arr1[index++] = i;
                base[i]--;
            }
        }
        return arr1;

    }
}
